#pragma once

////////////////////////////////////////////////////////////////////////////////////////
namespace react {

////////////////////////////////////////////////////////////////////////////////////////
/// BasicSingleton
////////////////////////////////////////////////////////////////////////////////////////
template <typename T>
class BasicSingleton
{
public:
	static T& Instance()
	{
		static T instance;
		return instance;
	}

protected:
	BasicSingleton() {}
	BasicSingleton(const BasicSingleton& other) {}
	BasicSingleton(BasicSingleton&& other) {}
};

} //~namespace react